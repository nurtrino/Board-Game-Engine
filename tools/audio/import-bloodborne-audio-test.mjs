import assert from 'node:assert/strict';
import { mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { tmpdir } from 'node:os';
import {
  importAudio,
  normalizeEntry,
  parseCli,
  validateRemoteAudioUrl,
} from './import-bloodborne-audio.mjs';
import { verifyBloodborneAudio } from './verify-bloodborne-audio.mjs';

assert.equal(validateRemoteAudioUrl('https://artist.example/music/theme.mp3').pathname, '/music/theme.mp3');
for (const rejected of [
  'https://youtube.com/watch?v=abc',
  'https://youtu.be/abc',
  'https://open.spotify.com/track/abc.mp3',
  'https://artist.example/music/theme',
  'ftp://artist.example/theme.ogg',
]) assert.throws(() => validateRemoteAudioUrl(rejected));

assert.throws(() => normalizeEntry({ input: 'song.wav', title: 'Song', roles: ['menu'], source: 'me' }), /license is required/);
assert.throws(() => normalizeEntry({ input: 'song.wav', title: 'Song', roles: ['menu'], license: 'unknown', source: 'me' }), /actual license/);
assert.throws(() => normalizeEntry({ input: 'song.wav', title: 'Song', roles: ['menu'], license: 'CC0', source: 'https:\/\/youtube.com\/watch?v=x' }), /source metadata/);
assert.deepEqual(parseCli(['--input', 'song.wav', '--role', 'menu,exploration', '--license', 'CC0', '--source', 'me']).entry.roles, ['menu', 'exploration']);

const temporary = await mkdtemp(join(tmpdir(), 'bge-audio-test-'));
try {
  const wav = join(temporary, 'tone.wav');
  await writeFile(wav, waveFile(8_000, 0.25));
  const outputDir = join(temporary, 'public-audio');
  const entry = normalizeEntry({
    input: wav,
    id: 'test-tone',
    title: 'Test Tone',
    roles: ['menu', 'exploration'],
    license: 'CC0-1.0',
    source: 'Generated by the importer unit test',
    attribution: 'Synthetic test tone',
    gain: 0.4,
    crossfadeMs: 123,
  });
  const result = await importAudio({ outputDir, entries: [entry], replace: false, dryRun: false });
  assert.equal(result.imported.length, 1);
  assert.match(result.imported[0].sha256, /^[a-f0-9]{64}$/);
  assert.ok(result.imported[0].bytes > 0);
  const manifest = JSON.parse(await readFile(join(outputDir, 'manifest.json'), 'utf8'));
  assert.equal(manifest.tracks[0].id, 'test-tone');
  assert.deepEqual(manifest.tracks[0].roles, ['menu', 'exploration']);
  assert.match(await readFile(join(outputDir, 'CREDITS.md'), 'utf8'), /Synthetic test tone/);
  assert.equal((await verifyBloodborneAudio(outputDir)).tracks.length, 1);
  await assert.rejects(
    importAudio({ outputDir, entries: [entry], replace: false, dryRun: false }),
    /already exists/,
  );
} finally {
  await rm(temporary, { recursive: true, force: true });
}

console.log('bloodborne audio importer tests passed');

function waveFile(sampleRate, seconds) {
  const samples = Math.floor(sampleRate * seconds);
  const dataBytes = samples * 2;
  const buffer = Buffer.alloc(44 + dataBytes);
  buffer.write('RIFF', 0);
  buffer.writeUInt32LE(36 + dataBytes, 4);
  buffer.write('WAVEfmt ', 8);
  buffer.writeUInt32LE(16, 16);
  buffer.writeUInt16LE(1, 20);
  buffer.writeUInt16LE(1, 22);
  buffer.writeUInt32LE(sampleRate, 24);
  buffer.writeUInt32LE(sampleRate * 2, 28);
  buffer.writeUInt16LE(2, 32);
  buffer.writeUInt16LE(16, 34);
  buffer.write('data', 36);
  buffer.writeUInt32LE(dataBytes, 40);
  for (let i = 0; i < samples; i++) {
    buffer.writeInt16LE(Math.round(Math.sin(i / sampleRate * Math.PI * 2 * 220) * 3_000), 44 + i * 2);
  }
  return buffer;
}
