// Assemble shared/src/trek/data.json for the Trekking engine from the golden
// extraction (trails + card costs) and the staged scene (deck cell mapping).
// Run: node tools/tts-extract/gen-trek-data.mjs

import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(import.meta.dirname, '../..');
const read = (p) => JSON.parse(fs.readFileSync(path.join(ROOT, p), 'utf8'));
const bd = read('shared/src/trek/board-data.json');
const trails = read('games/trekking/golden/trails.json');
const cards = read('games/trekking/golden/cards.json');
const scene = read('client/public/trek/scene.json');

// board node name -> card name aliases (mod typos / diacritics / plurals)
const alias = {
  'Grand Cayon': 'Grand Canyon', 'Acadia ': 'Acadia',
  'Grand Tetons': 'Grand Teton', 'Haleakalā': 'Haleakala',
  "Hawai'i Volcanoes": "Hawai'i Volcanoes",
};
const canon = (s) => alias[s] ?? s;

// world -> art px (board rotY 180, scale 4; art 9000x4500)
const toPx = ([x, , z]) => [
  Math.round(((x / 4 + 12) / 24) * 9000),
  Math.round((1 - (6 + z / 4) / 12) * 4500),
];

// nodes: 0 = START (virtual), 24..68 = park snaps (1-based snap index)
const nodes = { 0: { name: 'START', px: trails.start.px, major: false } };
for (const [id, name] of Object.entries(trails.names)) {
  nodes[id] = { name, px: toPx(bd.snaps[id - 1].pos), major: trails.majors.includes(Number(id)) };
}
const nodeByName = Object.fromEntries(Object.entries(nodes).map(([id, n]) => [canon(n.name), Number(id)]));

// trek catalog: 24 unique faces from the deck's (name, cell) pairs
const suits = ['Blue', 'Yellow', 'Purple', 'Red', 'Green', 'Brown'];
const faceCell = new Map();
scene.decks.trek.names.forEach((nm, i) => {
  const cell = scene.decks.trek.cards[i].cell;
  if (faceCell.has(nm) && faceCell.get(nm) !== cell) throw new Error(`trek face conflict ${nm}`);
  faceCell.set(nm, cell);
});
if (faceCell.size !== 24) throw new Error(`expected 24 trek faces, got ${faceCell.size}`);
const trekCatalog = [];
for (const suit of suits) for (let value = 1; value <= 4; value++) {
  const nm = `${suit} ${value}`;
  if (!faceCell.has(nm)) throw new Error(`missing trek face ${nm}`);
  trekCatalog.push({ suit, value, cell: faceCell.get(nm) });
}

// park cards: 39, ordered by sheet cell for a stable id
const parks = scene.decks.parks.names.map((raw, i) => {
  const m = raw.match(/^(.*?)\s*-(\d\d)Park$/);
  if (!m) throw new Error(`bad park name ${raw}`);
  const name = m[1], vp = Number(m[2]);
  const cost = cards.parkCosts[name];
  const node = nodeByName[canon(name)];
  if (!cost) throw new Error(`no cost for park ${name}`);
  if (node === undefined) throw new Error(`no board node for park ${name}`);
  return { name, vp, cost, cell: scene.decks.parks.cards[i].cell, node };
}).sort((a, b) => a.cell - b.cell);

// majors: 6, ability keyed by canonical name
const abilities = {
  'Grand Canyon': 'plusOneMove', 'Acadia': 'wildPairs', 'Everglades': 'stoneSwap',
  "Hawai'i Volcanoes": 'freeHop', 'Denali': 'drawTwo', 'Yellowstone': 'drawOnClaim',
};
const majors = scene.decks.majors.names.map((raw, i) => {
  const name = canon(raw);
  const cost = cards.majorCosts[name];
  const node = nodeByName[name];
  if (!cost || node === undefined || !abilities[name]) throw new Error(`bad major ${raw}`);
  return { name, cost, cell: scene.decks.majors.cards[i].cell, node, ability: abilities[name] };
}).sort((a, b) => a.cell - b.cell);

// sanity: VP distribution and cost sizes
const byVp = { 5: 0, 7: 0, 10: 0 };
for (const p of parks) {
  byVp[p.vp]++;
  const want = { 5: 2, 7: 3, 10: 4 }[p.vp];
  if (p.cost.length !== want) throw new Error(`${p.name}: vp ${p.vp} but ${p.cost.length} icons`);
  for (const s of p.cost) if (!suits.includes(s)) throw new Error(`${p.name}: bad suit ${s}`);
}
if (byVp[5] !== 13 || byVp[7] !== 16 || byVp[10] !== 10) throw new Error(`vp distribution ${JSON.stringify(byVp)}`);
const parkNodes = new Set(parks.map((p) => p.node));
if (parkNodes.size !== 39) throw new Error('park node collision');
for (const m of majors) if (parkNodes.has(m.node)) throw new Error(`major ${m.name} collides with a park card node`);

const out = {
  source: 'Generated by tools/tts-extract/gen-trek-data.mjs from golden trails/cards + staged scene. Do not hand-edit.',
  seats: bd.seats,
  suits,
  stoneCount: bd.stoneCount,
  scoring: bd.scoring,
  nodes,
  edges: trails.edges,
  trekCatalog,
  parks,
  majors,
};
fs.writeFileSync(path.join(ROOT, 'shared/src/trek/data.json'), JSON.stringify(out, null, 1));
console.log(`data.json: ${Object.keys(nodes).length} nodes, ${trails.edges.length} edges, ${parks.length} parks, ${majors.length} majors, ${trekCatalog.length} trek faces`);
