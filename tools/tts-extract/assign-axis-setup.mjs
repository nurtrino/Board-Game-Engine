// Build the engine's map + setup data from the two goldens:
//  - games/axis-allies/golden/map.json (board transcription; center {px,py},
//    ONE mixed adj list per space, canals {sea, land}) is normalized into the
//    engine schema (tuple centers, adj/coastTo split, canal connects/controlledBy)
//    -> shared/src/axis/map-data.json
//  - games/axis-allies/golden/setup-{1941,1942}.raw.json (packup-bag world
//    transforms) are zone-assigned via map.worldFit -> shared/src/axis/setup-data.json
// Cross-checks computed national production against the rulebook's printed
// starting IPCs (hard gate: any mismatch exits 1).
// Run: node tools/tts-extract/assign-axis-setup.mjs

import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(import.meta.dirname, '../..');
const GOLD = path.join(ROOT, 'games/axis-allies/golden');
const map = JSON.parse(fs.readFileSync(path.join(GOLD, 'map.json'), 'utf8'));
const asiaSetupCorrections = JSON.parse(fs.readFileSync(path.join(GOLD, 'setup-asia-corrections.json'), 'utf8'));
const F = map.worldFit;
const toPx = (wx, wz) => [F.a * wx + F.b * wz + F.e, F.c * wx + F.d * wz + F.f];

const isSz = (id) => id.startsWith('sz-');

// Printed-roundel positions where they sit far from the geometric center
// (elongated archipelagos) — read from art crops. TTS pieces/markers were
// placed ON the roundels, so distance checks consider these too.
const EXTRA_ANCHORS = {
  'new-guinea': [[7700, 4120]],
  'solomon-islands': [[8290, 4230]],
};
const SEA_UNITS = new Set(['battleship', 'carrier', 'cruiser', 'destroyer', 'submarine', 'transport']);

// Control at setup is derived from data, not hand lists:
//  1. printed roundel (originalOwner),
//  2. overridden where a power's LAND units stand at setup in a territory of
//     the other side (the mod starts Japanese infantry on Manchuria/Kiangsu
//     over the printed KMT roundels — the physical 1941 baseline),
//  3. overridden by the 1942 packup's occupation markers, assigned
//     nearest-first with each marker taking a distinct territory.
// Validation gates:
//  - 1941 starting cash EQUALS production by design (chart p9): computed
//    production must match the printed 31/17/10/40/43/30 exactly.
//  - Both scenarios' victory-city token lists must match the chart exactly
//    (1942's cash chart is cash-only; production differs, so VC lists are
//    the 1942 ownership gate).
const EXPECTED_IPCS_1941 = { germany: 31, japan: 17, italy: 10, usa: 40, uk: 43, ussr: 30 };
const AXIS_SIDE = new Set(['germany', 'italy', 'japan']);
const sideOf = (p) => (p === 'china' ? 'allies' : AXIS_SIDE.has(p) ? 'axis' : 'allies');
// chart pp8-9: victory-city TOKEN holders per scenario
const EXPECTED_VC = {
  1941: {
    germany: ['Berlin', 'Paris', 'Warsaw'],
    japan: ['Tokyo', 'Shanghai'],
    italy: ['Rome'],
    usa: ['Washington', 'San Francisco', 'Honolulu', 'Manila'],
    uk: ['London', 'Calcutta', 'Sydney', 'Hong Kong', 'Ottawa'],
    ussr: ['Moscow', 'Stalingrad', 'Leningrad'],
  },
  1942: {
    germany: ['Berlin', 'Paris', 'Warsaw'],
    japan: ['Tokyo', 'Shanghai', 'Hong Kong', 'Manila'],
    italy: ['Rome'],
    usa: ['Washington', 'San Francisco', 'Honolulu'],
    uk: ['London', 'Calcutta', 'Sydney', 'Ottawa'],
    ussr: ['Moscow', 'Stalingrad', 'Leningrad'],
  },
};
const LAND_UNITS = new Set(['infantry', 'artillery', 'tank']);

// ---------- 1. normalize the map into the engine schema ----------

const engineTerritories = map.territories.map((t) => ({
  id: t.id,
  name: t.name,
  ipc: t.ipc,
  originalOwner: t.originalOwner === 'neutral' ? null : t.originalOwner ?? null,
  ...(t.isVictoryCity ? { isVictoryCity: true, victoryCity: t.victoryCityName } : {}),
  ...(t.isCapital ? { isCapital: true } : {}),
  ...(t.isImpassable ? { isImpassable: true } : {}),
  ...(t.originalOwner === 'china' ? { isChinese: true } : {}),
  center: [t.center.px, t.center.py],
  adj: t.adj.filter((a) => !isSz(a)),
  coastTo: t.adj.filter(isSz),
}));
const engineSeaZones = map.seaZones.map((z) => ({
  id: z.id,
  n: z.number,
  center: [z.center.px, z.center.py],
  adj: z.adj.filter(isSz),
  coastTo: z.adj.filter((a) => !isSz(a)),
}));
const engineCanals = map.canals.map((c) => ({
  id: c.id,
  connects: c.sea,
  controlledBy: c.land, // turkish-straits gates on Turkey, an impassable
  // neutral no side ever controls -> permanently closed, which is the rule
}));
const engineMap = {
  _note: 'Generated by tools/tts-extract/assign-axis-setup.mjs from games/axis-allies/golden/map.json. Do not hand-edit.',
  territories: engineTerritories,
  seaZones: engineSeaZones,
  canals: engineCanals,
  art: { width: map.art.width, height: map.art.height },
};

// ---------- 2. zone-assign the setups ----------

const dist2 = (a, b) => (a[0] - b[0]) ** 2 + (a[1] - b[1]) ** 2;
function pointsOf(c) {
  return [c.center, ...(EXTRA_ANCHORS[c.id] ?? [])];
}
function nearest(px, candidates) {
  let best = null;
  let bestD = Infinity;
  for (const c of candidates) {
    for (const pt of pointsOf(c)) {
      for (const dx of [0, -map.art.width, map.art.width]) {
        const d = dist2([px[0] + dx, px[1]], pt);
        if (d < bestD) { bestD = d; best = c; }
      }
    }
  }
  return { space: best, dist: Math.sqrt(bestD) };
}

const out = {};
let failed = false;

for (const scen of ['1941', '1942']) {
  const raw = JSON.parse(fs.readFileSync(path.join(GOLD, `setup-${scen}.raw.json`), 'utf8'));
  const units = {};
  const flagged = [];

  const put = (space, power, key, count, cargo) => {
    const list = (units[space] ??= []);
    const hit = !cargo?.length && list.find((s) => s.power === power && s.key === key && !s.cargo);
    if (hit) hit.count += count;
    else list.push({ power, key, count, ...(cargo?.length ? { cargo } : {}) });
  };

  // control first (unit power backfill for AA/IC needs it)
  const control = {};
  for (const t of engineTerritories) {
    if (t.isImpassable) continue;
    control[t.id] = t.originalOwner ?? null;
  }
  const terrById = Object.fromEntries(engineTerritories.map((t) => [t.id, t]));

  // occupation markers: nearest-first, DISTINCT territories; a marker may only
  // take a victory city its nation is chartered to hold in this scenario
  const vcAllowed = (nation, t) => !t.isVictoryCity || (EXPECTED_VC[scen][nation] ?? []).includes(t.victoryCity);
  // global min-cost matching (markers x distinct territories) — greedy fails
  // when a cluster of markers straddles neighboring centers (Philippines /
  // New Guinea / Solomons)
  const markerCands = (raw.markers ?? []).map((m) => {
    const px = toPx(m.pos[0], m.pos[1]);
    const cands = engineTerritories
      .filter((t) => !t.isImpassable && vcAllowed(m.nation, t))
      .map((t) => {
        let d = Infinity;
        for (const pt of pointsOf(t)) {
          for (const dx of [0, -map.art.width, map.art.width]) d = Math.min(d, dist2([px[0] + dx, px[1]], pt));
        }
        return { id: t.id, d: Math.sqrt(d) };
      })
      .sort((a, b) => a.d - b.d)
      .slice(0, 4)
      // garrison evidence: an occupied territory has the occupier's land
      // units nearby in the raw setup; candidates without any get a heavy
      // penalty (kills phantom picks like New Zealand)
      .map((c) => {
        const t = terrById[c.id];
        const garrison = raw.units.some((u) => {
          if (!LAND_UNITS.has(u.unit) || u.nation !== m.nation) return false;
          const upx = toPx(u.pos[0], u.pos[1]);
          let d = Infinity;
          for (const pt of pointsOf(t)) {
            for (const dx of [0, -map.art.width, map.art.width]) d = Math.min(d, dist2([upx[0] + dx, upx[1]], pt));
          }
          return Math.sqrt(d) < 700;
        });
        return { ...c, d: c.d + (garrison ? 0 : 2000) };
      })
      .sort((a, b) => a.d - b.d);
    return { nation: m.nation, cands };
  });
  // chart constraint: every VC the chart hands to a nation that base control
  // doesn't already give it MUST be covered by one of that nation's markers
  const requiredVc = [];
  for (const [nation, cities] of Object.entries(EXPECTED_VC[scen])) {
    for (const t of engineTerritories) {
      if (t.isVictoryCity && cities.includes(t.victoryCity) && control[t.id] !== nation) {
        requiredVc.push({ nation, id: t.id });
      }
    }
  }
  // order by fewest options first for tighter pruning
  const order = markerCands.map((_, i) => i).sort((a, b) => markerCands[a].cands.length - markerCands[b].cands.length);
  let bestMatch = null;
  const used = new Set();
  const chosen = [];
  const covered = () => requiredVc.every((r) =>
    chosen.some((c, k) => c.id === r.id && markerCands[order[k]].nation === r.nation));
  const solve = (k, cost) => {
    if (bestMatch && cost >= bestMatch.cost) return;
    if (k === order.length) {
      if (covered()) bestMatch = { cost, pick: [...chosen] };
      return;
    }
    const m = markerCands[order[k]];
    for (const c of m.cands) {
      if (used.has(c.id)) continue;
      used.add(c.id); chosen.push(c);
      solve(k + 1, cost + c.d);
      used.delete(c.id); chosen.pop();
    }
  };
  solve(0, 0);
  if (!bestMatch && markerCands.length) {
    console.error('marker matching found no distinct assignment');
    failed = true;
  }
  if (bestMatch) {
    bestMatch.pick.forEach((c, k) => {
      const m = markerCands[order[k]];
      control[c.id] = m.nation;
      flagged.push({ note: 'marker', space: c.id, to: m.nation, dist: Math.round(c.d) });
    });
  }

  const placed = [];
  // candidates judged against control AFTER markers, so 1942 Japanese
  // garrisons can stand on captured Allied islands
  const landCandidatesFor = (nation) => engineTerritories.filter((t) => {
    if (t.isImpassable) return false;
    const owner = control[t.id] ?? t.originalOwner;
    if (!owner || owner === 'neutral') return false;
    if (owner === 'china') return nation === 'china' || nation === 'japan';
    if (!nation) return true; // unowned AA/IC: any real territory
    return sideOf(owner) === sideOf(nation);
  });
  for (const u of raw.units) {
    const px = toPx(u.pos[0], u.pos[1]);
    const sea = SEA_UNITS.has(u.unit);
    // aircraft at setup sit on friendly land like everything else (a UK
    // bomber cannot start in German Northwestern Europe — jitter artifact)
    const cands = sea ? engineSeaZones : landCandidatesFor(u.nation);
    const { space, dist } = nearest(px, cands);
    if (!space) continue;
    if (dist > 450) flagged.push({ nick: u.nick, kind: u.unit, space: space.id, dist: Math.round(dist) });
    const power = u.nation ?? null; // AA/IC power backfilled from FINAL control
    const cargo = (u.cargo ?? []).map((c) => ({ power: c.nation ?? u.nation ?? null, key: c.unit, count: 1 }));
    put(space.id, power, u.unit, 1, cargo);
    placed.push({ px, space: space.id, power, key: u.unit });
  }

  // presence rule: a china-roundel territory where one power's land units
  // outnumber everyone else's flips to that power (Japan on Manchuria/Kiangsu)
  for (const t of engineTerritories) {
    if (t.originalOwner !== 'china') continue;
    const counts = {};
    for (const st of units[t.id] ?? []) {
      if (!LAND_UNITS.has(st.key) || !st.power) continue;
      counts[st.power] = (counts[st.power] ?? 0) + st.count;
    }
    const ranked = Object.entries(counts).sort((a, b) => b[1] - a[1]);
    if (ranked.length && ranked[0][0] !== 'china' && ranked[0][1] >= 2 && ranked[0][1] > (ranked[1]?.[1] ?? 0)) {
      control[t.id] = ranked[0][0];
      flagged.push({ note: 'presence flip', space: t.id, to: ranked[0][0] });
    }
  }

  // backfill AA/IC ownership from final control
  for (const [spaceId, list] of Object.entries(units)) {
    for (const st of list) if (st.power == null) st.power = control[spaceId] ?? null;
  }
  void terrById;

  // chips multiply the nearest stack
  const chipValue = (nick) => (/Ten/i.test(nick) ? 10 : /Three/i.test(nick) ? 3 : 1);
  for (const chip of raw.chips ?? []) {
    const px = toPx(chip.pos[0], chip.pos[1]);
    let best = null;
    let bestD = Infinity;
    for (const p of placed) {
      const d = dist2(px, p.px);
      if (d < bestD) { bestD = d; best = p; }
    }
    if (!best) continue;
    const add = chipValue(chip.nick) * (chip.count ?? 1);
    const stack = units[best.space].find((s) => s.power === best.power && s.key === best.key);
    if (stack) stack.count += add;
    if (Math.sqrt(bestD) > 260) flagged.push({ nick: `${chip.nick} x${chip.count ?? 1}`, kind: 'chip', space: best.space, near: best.key, dist: Math.round(Math.sqrt(bestD)) });
  }

  // East Asia is too tightly packed for nearest-center assignment: several
  // physical pieces sit closer to the wrong side of a printed border. Apply
  // the national setup charts as an explicit, reviewed source after all raw
  // pieces/chips are counted. This also tags the U.S.-sculpt Flying Tigers as
  // China for rules authority while the renderer may still use U.S. artwork.
  const asia = asiaSetupCorrections[scen];
  if (!asia) throw new Error(`Missing official East Asia correction for ${scen}`);
  for (const [space, stacks] of Object.entries(asia.units)) {
    if (stacks.length > 0) units[space] = stacks.map((stack) => ({ ...stack }));
    else delete units[space];
  }
  for (const [space, holder] of Object.entries(asia.control)) control[space] = holder;

  out[scen] = { units, control };

  // ---------- cross-checks ----------
  console.log(`--- ${scen} checks ---`);
  if (scen === '1941') {
    // 1941: starting cash equals production by design
    const prod = {};
    for (const t of engineTerritories) {
      const owner = control[t.id];
      if (owner && owner !== 'china') prod[owner] = (prod[owner] ?? 0) + t.ipc;
    }
    for (const [p, expect] of Object.entries(EXPECTED_IPCS_1941)) {
      const got = prod[p] ?? 0;
      const ok = got === expect;
      if (!ok) failed = true;
      console.log(`production ${p}: computed ${got}, rulebook ${expect} ${ok ? 'OK' : '*** MISMATCH ***'}`);
    }
  }
  // both scenarios: victory-city token lists must match the chart
  for (const [p, cities] of Object.entries(EXPECTED_VC[scen])) {
    const held = engineTerritories.filter((t) => t.isVictoryCity && control[t.id] === p).map((t) => t.victoryCity).sort();
    const expect = [...cities].sort();
    const ok = JSON.stringify(held) === JSON.stringify(expect);
    if (!ok) failed = true;
    console.log(`VC ${p}: [${held.join(', ')}] ${ok ? 'OK' : `*** expected [${expect.join(', ')}] ***`}`);
  }
  const totalUnits = Object.values(units).flat().reduce((n, s) => n + s.count + (s.cargo?.reduce((m, c) => m + c.count, 0) ?? 0), 0);
  const rawTotal = raw.units.length + (raw.chips ?? []).reduce((n, c) => n + chipValue(c.nick) * (c.count ?? 1), 0) + raw.units.reduce((n, u) => n + (u.cargo?.length ?? 0), 0);
  console.log(`${scen}: ${totalUnits} units placed (raw pieces+chips = ${rawTotal}) across ${Object.keys(units).length} spaces; ${flagged.length} flagged`);
  for (const f of flagged) console.log('  review:', JSON.stringify(f));
  fs.writeFileSync(path.join(GOLD, `setup-${scen}.json`), JSON.stringify({ ...out[scen], review: flagged }, null, 1));
}

fs.writeFileSync(path.join(ROOT, 'shared/src/axis/map-data.json'), JSON.stringify(engineMap, null, 1));
fs.writeFileSync(
  path.join(ROOT, 'shared/src/axis/setup-data.json'),
  JSON.stringify({ _note: 'Generated by tools/tts-extract/assign-axis-setup.mjs. Do not hand-edit.', ...out }, null, 1),
);
console.log('written: shared/src/axis/map-data.json + setup-data.json + golden setup-*.json');
if (failed) {
  console.error('PRODUCTION CROSS-CHECK FAILED — inspect ownership/marker assignment before shipping.');
  process.exit(1);
}
